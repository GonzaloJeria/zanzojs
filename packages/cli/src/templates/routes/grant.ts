/**
 * Template: API route — POST /api/grant
 * Grants a permission by inserting base + derived tuples into the database.
 */

import type { FrameworkType } from './permissions';

export function grantRouteTemplate(framework: FrameworkType, orm: string): string {
  switch (framework) {
    case 'nextjs-app':
      return nextjsAppGrant(orm);
    case 'nextjs-pages':
      return nextjsPagesGrant(orm);
    case 'express':
      return expressGrant(orm);
    case 'hono':
      return honoGrant(orm);
    case 'other':
      return otherGrant(orm);
  }
}

